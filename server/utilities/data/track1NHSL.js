import {guid} from './../uuid';
let TrackModel = require("./../../timps/api/track/track.model");

export function addtrack1NHSLAssets()
{
    TrackModel.findOne({ trackId: "SEPTA-NT-01" }, function(err, track) {
    if(track && track.units && track.units.length<40) 
    {
       nhslAssets.forEach((asset)=>{
       asset.id = guid();
       asset.start = "" + asset.start;
       asset.end=asset.start;
       asset.assetLength="0";
       });
      track.units = [...track.units, ...nhslAssets] 
      track.markModified("units");
      track.save();
      console.log("added NHSL track assets to SEPTA-NT-01");
    }
  });

}

