const trackDataAssets = trackData => {
  let track1Obj = [];
  trackData.forEach(asset => {
    let lat = "";
    let lon = "";
    if (asset.D !== "") {
      let split = asset.D.split(",");
      lat = split[0];
      lon = split[1];
    }
    let newAsset = {
      assetType: "Switch",
      end: asset.A,
      start: asset.A,
      assetLength: "0",
      pastIssue: "",
      lastInspection: "",
      nextInspection: "",
      lonStart: "",
      lonEnd: "",
      latStart: "",
      latEnd: "",
      unitId: asset.B,
      coordinates: [[lat, lon], ["", ""]],
      id: guid(),
      createdAt: new Date(),
      updatedAt: new Date(),
    };
    track1Obj.push(newAsset);
  });
  // NAME ASSETID OF SAME NAME WITH MilePOST
  track1Obj.forEach(asset => {
    if (asset.unitId.includes("Stone Xing")) {
      asset.assetType = "Stone Xing";
    } else if (asset.unitId.includes("speed change")) {
      asset.assetType = "speed change";
    } else if (asset.unitId.includes("Gas Pipeline")) {
      asset.assetType = "Gas Pipeline";
    } else if (asset.unitId.includes(" Norristown Station")) {
      asset.assetType = " Norristown Station";
    } else if (asset.unitId.includes("Platform")) {
      asset.assetType = "Platform";
    }
    asset.unitId = asset.unitId + "-" + asset.start;
  });
  return track1Obj;
};

